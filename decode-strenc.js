// Decode StrEnc XOR: str ^ key
function xor(str, keyBuf) {
  let out = '';
  for (let i = 0; i < str.length && i < keyBuf.length; i++) {
    out += String.fromCharCode(str.charCodeAt(i) ^ keyBuf[i]);
  }
  return out;
}

